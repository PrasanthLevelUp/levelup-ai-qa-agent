/**
 * ============================================================================
 * GOLD BENCHMARKS — Test Case Lab Intelligence Audit (Phase 1)
 * ============================================================================
 *
 * SEALED SENIOR-QA EXPECTATIONS.
 *
 * These were authored from the requirement text + senior QA expertise ALONE,
 * BEFORE looking at any generated AI output. They encode what a QA Lead with
 * 15 years of experience would expect the suite to validate.
 *
 * RULE: Do NOT modify these expectations after seeing AI output. They are the
 * fixed yardstick. If the generator improves, it improves toward THIS — the
 * benchmark does not move toward the generator.
 *
 * Each expected validation is a BUSINESS VALIDATION CONCEPT (not a P/N/E label).
 * `match` holds lowercase keyword alternatives; the harness marks a validation
 * "covered" when a generated scenario's title/objective contains any of them.
 * `weight` marks whether missing it is Critical / High / Medium for scoring.
 * ============================================================================
 */

import type { RequirementInput } from '../src/engines/test-coverage-engine';

export type Weight = 'critical' | 'high' | 'medium';

export interface GoldValidation {
  /** Business-validation grouping, e.g. "Mandatory", "Authorization". */
  group: string;
  /** Human name of the expected validation. */
  name: string;
  /** Keyword alternatives (lowercase) used to detect coverage in output. */
  match: string[];
  /** How costly it is to miss this validation. */
  weight: Weight;
}

export interface GoldBenchmark {
  id: string;
  label: string;
  requirement: RequirementInput;
  /** The KB category a senior QA would expect this to route to (classification check). */
  expectedCategory: string;
  expected: GoldValidation[];
}

const V = (group: string, name: string, match: string[], weight: Weight = 'high'): GoldValidation => ({
  group,
  name,
  match: match.map((m) => m.toLowerCase()),
  weight,
});

